def calculate_rsi(klines: list, period: int = 14) -> float:
    """
    Вычисляет RSI по последним `period + 1` свечам.

    :param klines: Список свечей от старых к новым.
    :param period: Период RSI.
    :return: RSI-значение.
    """

    def rma(values: list[float], period: int) -> list[float]:
        """Wilder's RMA (аналог ta.rma на TradingView)"""
        result = []
        avg = sum(values[:period]) / period
        result.append(avg)
        for val in values[period:]:
            avg = (avg * (period - 1) + val) / period
            result.append(avg)
        return result

    if len(klines) < period + 1:
        raise ValueError("Not enough data to calculate RSI")

    closes = [k["c"] for k in klines]
    changes = [closes[i] - closes[i - 1] for i in range(1, len(closes))]
    gains = [max(c, 0) for c in changes]
    losses = [abs(min(c, 0)) for c in changes]

    avg_gains = rma(gains, period)
    avg_losses = rma(losses, period)

    last_gain = avg_gains[-1]
    last_loss = avg_losses[-1]

    if last_loss == 0:
        return 100.0
    if last_gain == 0:
        return 0.0

    rs = last_gain / last_loss
    rsi = 100 - (100 / (1 + rs))

    # Debug print
    # logger.warning("\n== Debug RSI source data ==")
    # for k in klines:
    #     from datetime import datetime

    #     dt = datetime.fromtimestamp(k["t"] / 1000)
    #     logger.info(f"{dt:%Y-%m-%d %H:%M:%S} — close: {k['c']}")
    # logger.info("==============================\n")
    # logger.info(f"RSI = {rsi:.2f} | gain = {last_gain:.2f}, loss = {last_loss:.2f}")

    return rsi


def is_valid_kline_interval(klines: list) -> bool:
    """Проверяет, что все свечи идут с одинаковым интервалом времени."""
    if len(klines) < 2:
        return True  # Меньше двух — нечего сравнивать

    intervals = [klines[i + 1]["t"] - klines[i]["t"] for i in range(len(klines) - 1)]
    first = intervals[0]
    for i, delta in enumerate(intervals):
        if delta != first:
            print(f"[Ошибка] Разница между свечами {i} и {i + 1} = {delta} мс, ожидается {first}")
            return False
    return True


